//1. Program to Take 2 Numbers and Print Sum
import java.util.Scanner;
public class SumOf2Num {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b= sc.nextInt();
        int sum = a + b;
        System.out.println(sum);
    }
}
